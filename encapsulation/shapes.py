class Rectangle:
    def __init__(self, width, height):
        self.width = width
        self.height = height


class Triangle:
    def __init__(self, side1, side2, side3):
        self.side1 = side1
        self.side2 = side2
        self.side3 = side3


class Circle:
    def __init__(self, radius):
        self.radius = radius
